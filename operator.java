class Operator
{
	
	public static void main(String[] args) 
	{


		int a=10;
		int b=2;
		//10+2=12
		int res = a+b;
		int min = a-b;
		int div = a/b;
		int mod = a%b;
		System.out.println("a+b = "+ res);
		System.out.println("a-b = "+ min);
		System.out.println("a/b = "+ div);
		System.out.println("a%b = "+ mod);
		System.out.println("a+b = "+ a+b);
		System.out.println(a+b+"= a+b");
	}
}
